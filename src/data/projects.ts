export const getProjectCategories = (): ProjectCategory[] => {
  return ['renovatie', 'nieuwbouw', 'verduurzaming', 'kozijnen', 'dakkapel', 'aanbouw'];
};

export const getProjectBySlug = (slug: string): Project | undefined => {
  return projects.find(project => project.slug === slug);
};

export const getRelatedProjects = (currentProjectId: string, category: string, count: number = 3): Project[] => {
  return projects
    .filter(project => project.id !== currentProjectId && project.category === category)
    .sort(() => Math.random() - 0.5)
    .slice(0, count);
};
